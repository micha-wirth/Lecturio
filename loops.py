# for x in list
for x in [1, 2, 3]:
    print(x)

# for c in string
for c in 'abc':
    print(c)

# for i in range(0, 3, 1):
for i in range(3):
    print(i)

print(list(range(3)))

# while-loop
x = 0
while x < 3:
    print(x)
    x += 1

# break-statement
while True:
    if x == 3:
        print('End of while-loop')
        break

# continue-statement
for x in range(3):
    if x == 1:
        continue
    print(x)




